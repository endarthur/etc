function getExportNovel() {
  const novel = STATE.novels[STATE.currentNovel];
  if (!novel || !novel.chapters.length) {
    alert('No novel selected or no chapters generated. Select a novel in the Library first.');
    return null;
  }
  return novel;
}

function sanitizeFilename(title) {
  return (title || 'novel').replace(/[^a-zA-Z0-9_-]/g, '_').slice(0, 40);
}

function downloadBlob(blob, filename) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  setTimeout(() => { URL.revokeObjectURL(url); a.remove(); }, 100);
}

